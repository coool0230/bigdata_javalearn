package com.day2.com;
public class WhileDemo {
	public static void main(String[] args){
		int i = 0;
		//while 循环
		while (i < 10){
			System.out.println(i);
			i ++;
		}
		i = 0;
		do{
			System.out.println(i);
			i ++;
			}
		while (i < 10);
		
		
	}
}
