package com.day2.com;
public class MonthDemp {
			public static void main(String[] args){
				int m = 10;
				if (m < 10){
					System.out.println("小于十月份");
				}
				else if (m > 10){
					System.out.println("大于十月份");
				}
				else{
					System.out.println("等于十月份");
				}
			}
}
