package com.meego.cainiao.Array;
public class LengthOfArray {

	public static void main(String[] args) {
	      String[][] data = new String[2][5];
	      System.out.println("第一维数组长度: " + data.length);
	      System.out.println("第二维数组长度: " + data[0].length);
	}

}

